#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{

    int num = 6;

    printf("%f\n", 8.9);

    printf("%f\n", 5.0 + 4.5);

    printf("%d\n", num);

    printf("%f\n", pow(2, 3));

    printf("%f\n", sqrt(36));

    printf("%f\n", ceil(36.356)); //round up
    
    printf("%f\n", floor(36.656)); //round down

    return 0;
}